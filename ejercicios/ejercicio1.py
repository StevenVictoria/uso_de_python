'''
Ejercicio 1: Operadores Aritméticos

Problema: Escribe un programa que solicite al usuario dos números y 
realice las operaciones de suma, resta, multiplicación, división y módulo.

'''
#numeros al usuario
num1 = float(input("ingrese el primer numero: "))
num2 = float(input("ingrese el segundo numero: "))

#realizar operaciones
suma=num1 + num2
print("el resultado de la suma =",suma)
resta=num1-num2
print("el resultado de la resta =",resta)
mult=num1 * num2
print("el resultado de la multiplicacion =",mult) 
divi=num1 / num2 
print("el resultado de la division =",divi)
modulo=num1 % num2
print("el resultado del modulo =",modulo)





